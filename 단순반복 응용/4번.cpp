#include<stdio.h>

int main()

{
	int m = 0, n = 0, k = 0, s = 0, i = 0, value = 0, c = 0, sum = 0;
	scanf("%d %d %d", &m, &n, &k);
	s = n - m;
	while ((s + 1) - i > 0)
	{
		if ((m + c) < n, (m + c) % k == 0)
		{
			value = m + c;
			sum = sum + value;
		}
		c = c + 1;
		i = i + 1;
	}
	printf("%d\n", sum);
}